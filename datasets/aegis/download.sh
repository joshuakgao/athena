# Download each player's file
for player in "${players[@]}"; do
    # Construct the download URL
    download_url="$base_url/$player.zip"
    
    # Output file path
    output_file="$download_dir/$player.zip"
    
    echo "Downloading $player..."
    
    # Use wget to download the file
    wget -q --show-progress -O "$output_file" "$download_url"
    
    # Check if download was successful (HTTP 200)
    if [ $? -eq 0 ]; then
        echo "Successfully downloaded $player"
    else
        echo "Failed to download $player (file may not exist)"
        rm -f "$output_file"  # Remove empty file if download failed
    fi
    
    # Small delay to be polite to the server
    sleep 1
done

