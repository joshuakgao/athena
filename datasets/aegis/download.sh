# Base URL for downloads
base_url="https://www.pgnmentor.com/events"

# Download directory
download_dir="datasets/aegis/raw_data"

# Download each player's file
for event in "${events[@]}"; do
    # Construct the download URL
    download_url="$base_url/$event.pgn"
    
    # Output file path
    output_file="$download_dir/$event.pgn"
    
    echo "Downloading $event..."
    
    # Use wget to download the file
    wget -q --show-progress -O "$output_file" "$download_url"
    
    # Check if download was successful (HTTP 200)
    if [ $? -eq 0 ]; then
        echo "Successfully downloaded $event"
    else
        echo "Failed to download $event (file may not exist)"
        rm -f "$output_file"  # Remove empty file if download failed
    fi
    
    # Small delay to be polite to the server
    sleep 1
done

# Unzip all downloaded player game zip files
for file in "$download_dir"/*.zip; do
  echo "Unzipping $file..."
  unzip -o "$file" -d "$download_dir"
  rm "$file"  # Remove the zip file after extraction
done
